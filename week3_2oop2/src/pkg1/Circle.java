package pkg1;

public class Circle {

    public static final double PI = 3.14;
    private int rad;

    public Circle() {
        this.rad = rad;
    }

    public Circle(int rad) {
        this.rad = rad;
    }

    public int getRad() {
        return rad;
    }

    public void setRod(int rad) {
        this.rad = rad;
    }

    public double getArea() {
        double area = 3.14 * rad * rad;
        return area;
    }

    public double getRound() {
        double round = 2 * 3.14 * rad;
        return round;
    }

}
